    <h2>Propósito:</h2>
    <p class="infoP">Esta Norma especifica os requisitos para estabelecer, implementar, manter e melhorar de
        forma contínua um sistema de gestão de segurança da informação dentro do contexto da organização.
        Esta Norma também inclui os requisitos para a avaliação e tratamento dos riscos de segurança da informação,
        ajustados às necessidades da organização. O propósito desta aplicação é possibilitar a identificação dos ativos e riscos
        associados, também os respetivos controlos identificados para minimizar o impacto dos riscos.
    </p>
    <p class="infoP">A ISO 27001 é complementada por um conjunto de outras normas entre as quais:</p>
    <ul>
        <li>ISO 27000 – Contextualização e Vocabulário;</li>
        <li>ISO 27002 – Código de Práticas;</li>
        <li>ISO 27004 – Avaliação da Eficácia do SGSI;</li>
        <li>ISO 27005 – Gestão de Risco;</li>
        <li>ISO 27006 – Requisitos para Auditores.</li>
    </ul>
    <h2>Motivação:</h2>
        <p class="infoP">As organizações interessadas no reconhecimento desta certificação pretendem:</p>
        <ul>
            <li>que a informação fique devidamente protegida;</li>
            <li>que a exposição a ameaças seja minimizado;</li>
            <li>minimizar o risco de negócio;</li>
            <li>demonstrar o seu compromisso com a gestão da segurança da informação;</li>
            <li>reconhecimento através da certificação.</li>
        </ul>
    <h2>Objetivos:</h2>
    <p class="infoP">Os principais objetivos da implementação da norma passa por:</p>
    <ul>
        <li>Identificar os requisitos de segurança da informação da organização, concretizados na definição da sua política de segurança;</li>
        <li>Implementar e utilizar os controlos apropriados à gestão dos riscos de segurança da informação enfrentados pela organização;</li>
        <li>Monitorizar e rever o desempenho e eficácia das medidas adoptadas;</li>
        <li>demonstrar o seu compromisso com a gestão da segurança da informação;</li>
        <li>Assegurar a melhoria contínua, baseada em medições objectivas.</li>
    </ul>
